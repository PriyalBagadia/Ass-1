# How Do You Traverse Through A Dictionary Object In Python?

my_dict = {'a': 1, 'b': 2, 'c': 3}

# Traverse through keys
for key in my_dict:
    print(key)

# Traverse through values
for value in my_dict.values():
    print(value)

# Traverse through key-value pairs
for key, value in my_dict.items():
    print(key, value)